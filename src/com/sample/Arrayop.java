package com.sample;
import java.util.*;
public class Arrayop {
    public static void main(String[] args)
    {
        ArrayList<Integer> cards = new ArrayList<Integer>();
        cards.add(1);
        cards.add(2);
        cards.add(3);
        for (int i : cards)
        {
            System.out.print(i);
        }
    }
}
